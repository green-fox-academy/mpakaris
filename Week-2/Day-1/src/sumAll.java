import java.util.Arrays;
import java.util.Arrays;

public class sumAll {
    public static void main(String[] args) {
        System.out.println();
        int[] ai = {3, 4, 5, 6, 7};
        int sum = 0;

        for (int i = 0; i < ai.length; i++) {
            sum = sum + ai[i];
        }

        System.out.println("The sum of all Elements is: " + sum);

    }
}
